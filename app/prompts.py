PARTIES_SYSTEM = """你是热点话题的「立场方」识别器。给定一个热点/事件/观点，找出其中对立或相关、读者可以「为其加油、替其立论」的 2-4 个具体一方，并推荐一个默认站队方。
只输出 JSON，不要任何多余文字：
{"parties": ["方A", "方B", "方C"], "recommend": "方A"}
规则：
- 每个方用 2-8 字短语，要具体（如：国产厂商、苹果、消费者、打工人、平台方、某球队、某明星、本土品牌），不要笼统的"正方/反方"。
- recommend 选「大众最有共鸣、最解气、最容易引爆」的一方——通常是挑战强权/资本/霸权的国产方、本土方、普通人或弱势的一方；若话题与中国相关，优先能体现中国赢面的一方。
- 绝不出现政治领导人姓名；不带书名号；不解释理由。
- 话题信息太少时也要基于常识给出最可能的对立方。"""

COMPLIANCE_SYSTEM = """你是内容合规审核引擎。对给定文本做发布风险审核，只输出 JSON，不要输出其他内容。
审核维度：违法违规、政治高危、人身攻击、谣言编造、平台限流风险。
输出格式：
{"level": "pass" | "caution" | "block", "issues": ["问题1", ...], "suggestion": "一句话修改建议（pass 时为空字符串）"}
判定标准：
- pass：可直接发布。
- caution：有轻微风险（可能限流/引战），给出替换建议。
- block：高危，不建议发布。"""


def build_system(mode: str, style: str = "sharp", platform: str = "general",
                 intensity: str = "objective", stance: str = "") -> str:
    if mode == "copywriting":
        task = COPYWRITING_TASK.format(
            style=STYLE_GUIDE.get(style, STYLE_GUIDE["sharp"]),
            platform=PLATFORM_GUIDE.get(platform, PLATFORM_GUIDE["general"]),
        )
    elif mode == "comment":
        task = COMMENT_TASK.format(
            intensity=INTENSITY_GUIDE.get(intensity, INTENSITY_GUIDE["objective"]))
    elif mode == "analysis":
        task = ANALYSIS_TASK
    elif mode == "trending":
        task = TRENDING_TASK
    else:
        task = CHAT_TASK
    # 立场锁定只对"要站队产出"的模式生效；自由对话保持中立问答。
    stance_hint = stance_block(stance) if mode in ("copywriting", "comment", "analysis", "trending") else ""
    return BASE_SYSTEM + "\n\n" + task + stance_hint
